package at.sti2.spark.handler;

public class SparkwaveHandlerException extends Exception {

	public SparkwaveHandlerException() {
	}

	public SparkwaveHandlerException(String arg0) {
		super(arg0);
	}

	public SparkwaveHandlerException(Throwable arg0) {
		super(arg0);
	}

	public SparkwaveHandlerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
