package at.sti2.spark.handler;

public class SparkweaveHandlerException extends Exception {

	public SparkweaveHandlerException() {
	}

	public SparkweaveHandlerException(String arg0) {
		super(arg0);
	}

	public SparkweaveHandlerException(Throwable arg0) {
		super(arg0);
	}

	public SparkweaveHandlerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
